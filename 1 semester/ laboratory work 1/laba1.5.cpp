#include <stdio.h>
#include <locale.h>
#define N 10
int main() {
  setlocale(LC_ALL, "");
  int dl, pr = 0, max = -9999999, min = 9999999;
  //вводим ширину и длину матрицы//
    do {
      pr += 1;
      if (pr > 1) {
        printf("Значение введено некорректно \n");
        printf("Введите значение больше предыдущего \n");
      }
      printf("Введите ширину матрицы - \n");
      scanf_s("%d", &dl);
    } while (dl < 1 or dl > 10);
  //задаем числа для матрицы//
  printf("Придумай числа для твоей матрицы - \n");
  int M[N][N] = { 0 };
  for (int i = 0; i < dl; i++) {
    for (int j = 0; j < dl; j++) {
      printf("co[%d][%d] - ", i, j);
      scanf_s("%d", &M[i][j]);
    }
  }
  //вывод саамой матрицы и числа 3 на 3 матриц//
  printf("\n");
  printf("Вот и сама матрица \n");
  for (int i = 0; i < dl; i++) {
    for (int j = 0; j < dl; j++) {
      if (j != i and j != dl-i-1) {
        if (M[i][j] > max) {
          max = M[i][j];
        }
        if (M[i][j] < min) {
          min = M[i][j];
        }
      }
      printf("%4d", M[i][j]);
    }
    printf("\n");
  }
  printf("\n");
  printf("Максимальный элемент - %d, минимальный элемент - %d", max, min);
  return 0;
}
